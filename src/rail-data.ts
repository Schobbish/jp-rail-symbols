type RailDataType = {
    /**
     * Object of data for the companies we're dealing with, indexed by the
     *  company abbreviations used on Wikipedia templates (or stuff I make up).
     */
    [companyAbbr: string]: CompanyDataType;
};
type CompanyDataType = {
    /** Aids readability and also may be useful. */
    fullCompanyName: string,
    /** Object of line data for this company, indexed by line abbreviation. */
    lines: {
        [lineAbbr: string]: LineDataType;
    };
};
type LineDataType = {
    /** Aids readability and also may be useful. */
    fullLineName: string,
    /**
     * Main color of this line and its symbols.
     * @see RailSymbolProps.symbolColor
     */
    symbolColor: string,
    /**
     * Color used for the line name (and station numbers if they are the same).
     * @see RailSymbolProps.textColor
     */
    textColor: string;
};

export default railData;
